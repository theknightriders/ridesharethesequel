            <p>The MGA KnightRiders is a transportation network system that is based in Macon, GA and operates on four other campuses which include: Cochran, Dublin, Warner Robins, and Eastman GA. Through an online website, faculty and staff are able to give and request rides from fellow colleagues at any hour of the day. This service also allows faculty and staff to post personal schedules and offer their vehicles as a carpool system. Members use their own cars. </p>

            <p>Our mission through this program is we hope to help all members of MGA by creating a system that allows them to save time, gas, and money when commuting from campus to campus. Along with this, we want to preserve our planet by saving resources and preventing pollution. Through launching this green initiative we hope to decrease the amount of cars and gas that is required when faculty and staff have to make multiple trips from campus to campus. The development of this service will help reduce the amount of pollutants that are sent into the air and will conserve resources. Additionally, through the use of the repeat option that we have implemented, this project will hopefully create lasting relationships and bring our MGA community together through the transportation network. Through the rideshare program, a significant amount of money can be saved by everyone involved, including the university. This is a huge factor and one of the main driving factors for this initiative.</p>

            <p>In summary, The MGA KnightRiders was a project set by forth the President of MGA and tasked to Dr. Scott Spangler. Under his guidance, the Senior Capstone class of Spring 2017 was able to design this system in order to help the faculty, staff, and future students to get from campus to campus by contacting fellow MGA personnel. </p>
